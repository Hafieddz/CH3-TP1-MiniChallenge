// Buatlah sebuah aplikasi node yang mampu melakukan operasi aritmetika dasar dengan internal module...

// Operasi Pertambahan
function add(a,b) {
    return a + b
}
// Operasi Pengurangan
function sub(a,b) {
    return a - b
}
// Operasi Pembagian
function div(a,b) {
    return a / b
}
// Operasi Perkalian
function mul(a,b) {
    return a * b
}

console.log(add(1,1)); // 2
console.log(sub(1,1)); // 0
console.log(div(1,1)); // 1
console.log(mul(1,1)); // 1
